const mongoose = require('mongoose');

//instantiate new model
const UserSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true
    },
    email: {
      type: String,
      required: true,
      unique: true
    },
    password: {
      type: String,
      required: true
    },
    avatar: {
      type: String
    },
    date: {
      type: Date,
      default: Date.now
    }
  })

//export the model
const User = mongoose.model('user', UserSchema);
module.exports = User;